# 2μ°¨μ›λ°°μ—΄ DFS/BFS μνλ¥Ό ν•λ ¤λ©΄
# delta λ°°μ—΄μ„(μΆν‘κ°’μ λ³€ν™”λ‰) μ΄μ©ν•΄ μνν•λ‹¤.
# μƒν•μΆμ° => 4λ°©
dr = [-1,1,0,0] #π‘©π»μ„Έλ΅
dc = [0,0,-1,1] #π‘©π»κ°€λ΅

r,c = 1,1
cnt = 1
for d in range(4): # π‘¨π»β€π«4λ°©μ΄λ‹κΉ 4λ² λλ‹¤.
    nr = r + dr[d] # nr: λ‹¤μ r, r: ν„μ¬ rowμ μΆν‘
    nc = c + dc[d] # nc: λ‹¤μ c, c: ν„μ¬ columnμ μΆν‘
    arr[nr][nc] = cnt
    cnt += 1
# λ‚μ™€ μ—°κ²°λμ–΄ μλ” μ”μ†λ¥Ό μ°ΎκΈ° μ„ν• λ°©λ²•: 4λ°© νƒμƒ‰
